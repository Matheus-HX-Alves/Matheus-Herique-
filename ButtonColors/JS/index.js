function ChangeColorDivRed(BackColor) {
    let BackgroundDiv = document.getElementById("MenuColor");
    BackgroundDiv.style.backgroundColor = BackColor;
}

function ChangeColorLiRed(BackColor) {
    let BackgroundLi = document.getElementById("ModeRed");
    BackgroundLi.style.backgroundColor = BackColor;
}

function ChangeBorderRed(BorderColor) {
    let li = document.getElementById("ModeRed");
    li.style.borderColor = BorderColor;
    li.style.color = BorderColor
}

